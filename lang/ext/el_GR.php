<li><p>Thirdly you should create some users.  Adding users is a two step process.  When creating a user you are asked for the username and password, and an expiry date for that user.  You then must select the groups that that user is a member of, and click \'Next\'</p>
<p><em>Note: </em>This is a labour intensive and boring process, it is easier to let users register themselves, and then you can edit their group information.  but for testing purposes, please create at lest one user</p></li>
<li><p>Lastly, after the system has determined all of the information fields to be presented for that user, you are presented with a form asking for all of the required user information.  Complete this form to complete the user addition</p></li>
<li><p>You are now ready to add the front end functionality to your site.  This is as simple as adding the {cms_module module=FrontEndUsers} tag to your page or template</p></li>
</ul>
